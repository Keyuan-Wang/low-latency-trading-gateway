#!/usr/bin/env bash
set -euo pipefail

# Configure boot-time CPU isolation on the remote benchmark host.
# This is intentionally aggressive and persistent until the GRUB drop-in is
# removed. It is meant for isolated benchmark servers.

SERVER_IP="${SERVER_IP:-}"
SSH_USER="${SSH_USER:-root}"
SSH_KEY="${SSH_KEY:-$HOME/.ssh/id_ed25519_hetzner}"
SSH_PORT="${SSH_PORT:-22}"

ISOLATED_CPUS="${ISOLATED_CPUS:-2,3}"
HOUSEKEEPING_CPUS="${HOUSEKEEPING_CPUS:-auto}"
REBOOT="${REBOOT:-1}"
WAIT_AFTER_REBOOT="${WAIT_AFTER_REBOOT:-1}"
SSH_WAIT_SECONDS="${SSH_WAIT_SECONDS:-240}"
LOCAL_OUT_DIR="${LOCAL_OUT_DIR:-./server_results}"

if [[ -z "$SERVER_IP" ]]; then
	echo "Usage: SERVER_IP=1.2.3.4 ISOLATED_CPUS=2,3 $0" >&2
	exit 1
fi

SSH_OPTS=(-i "$SSH_KEY" -p "$SSH_PORT" -o StrictHostKeyChecking=accept-new -o ConnectTimeout=10)
STAMP="$(date +%Y%m%d_%H%M%S)"
LOCAL_RESULT_DIR="$LOCAL_OUT_DIR/nohz_full_setup_$STAMP"
mkdir -p "$LOCAL_RESULT_DIR"

echo "===== Remote nohz_full setup ====="
echo "  Server        : ${SSH_USER}@${SERVER_IP}"
echo "  Isolated CPUs : $ISOLATED_CPUS"
echo "  Housekeeping  : $HOUSEKEEPING_CPUS"
echo "  Reboot        : $REBOOT"
echo ""

set +e
ssh "${SSH_OPTS[@]}" "${SSH_USER}@${SERVER_IP}" \
	"ISOLATED_CPUS='$ISOLATED_CPUS' HOUSEKEEPING_CPUS='$HOUSEKEEPING_CPUS' REBOOT='$REBOOT' bash -s" \
	> "$LOCAL_RESULT_DIR/setup.log" 2>&1 <<'ENDSSH'
set -euo pipefail

expand_cpu_list() {
	python3 - "$1" <<'PY'
import sys
out = []
for part in sys.argv[1].split(","):
	part = part.strip()
	if not part:
		continue
	if "-" in part:
		a, b = part.split("-", 1)
		out.extend(range(int(a), int(b) + 1))
	else:
		out.append(int(part))
print(",".join(map(str, sorted(set(out)))))
PY
}

subtract_cpu_list() {
	python3 - "$1" "$2" <<'PY'
import sys
def expand(text):
	res = set()
	for part in text.split(","):
		part = part.strip()
		if not part:
			continue
		if "-" in part:
			a, b = part.split("-", 1)
			res.update(range(int(a), int(b) + 1))
		else:
			res.add(int(part))
	return res
online = expand(sys.argv[1])
isolated = expand(sys.argv[2])
target = sorted(online - isolated)
if not target:
	raise SystemExit("empty housekeeping CPU set")
print(",".join(map(str, target)))
PY
}

run_as_root() {
	if [[ "$(id -u)" == "0" ]]; then
		"$@"
	else
		sudo "$@"
	fi
}

ONLINE_CPUS="$(cat /sys/devices/system/cpu/online)"
ISOLATED_CPUS="$(expand_cpu_list "$ISOLATED_CPUS")"
if [[ "$HOUSEKEEPING_CPUS" == "auto" ]]; then
	HOUSEKEEPING_CPUS="$(subtract_cpu_list "$ONLINE_CPUS" "$ISOLATED_CPUS")"
else
	HOUSEKEEPING_CPUS="$(expand_cpu_list "$HOUSEKEEPING_CPUS")"
fi

NOHZ_ARGS="nohz_full=$ISOLATED_CPUS rcu_nocbs=$ISOLATED_CPUS isolcpus=nohz,domain,managed_irq,$ISOLATED_CPUS irqaffinity=$HOUSEKEEPING_CPUS kthread_cpus=$HOUSEKEEPING_CPUS nowatchdog nmi_watchdog=0"
DROPIN="/etc/default/grub.d/99-llmes-nohz-full.cfg"
BACKUP_DIR="/root/llmes-nohz-full-backup-$(date +%Y%m%d_%H%M%S)"

echo "online_cpus=$ONLINE_CPUS"
echo "isolated_cpus=$ISOLATED_CPUS"
echo "housekeeping_cpus=$HOUSEKEEPING_CPUS"
echo "nohz_args=$NOHZ_ARGS"
echo "dropin=$DROPIN"
echo "backup_dir=$BACKUP_DIR"
echo

run_as_root mkdir -p "$BACKUP_DIR" /etc/default/grub.d
cat /proc/cmdline | run_as_root tee "$BACKUP_DIR/cmdline_before.txt" >/dev/null
[[ -f /etc/default/grub ]] && run_as_root cp -a /etc/default/grub "$BACKUP_DIR/grub"
[[ -d /etc/default/grub.d ]] && run_as_root cp -a /etc/default/grub.d "$BACKUP_DIR/grub.d"

{
	echo "# Generated by LLMES nohz_full benchmark setup."
	echo "# Remove this file and run update-grub to disable."
	printf 'LLMES_NOHZ_FULL_ARGS="%s"\n' "$NOHZ_ARGS"
	echo 'GRUB_CMDLINE_LINUX_DEFAULT="$GRUB_CMDLINE_LINUX_DEFAULT $LLMES_NOHZ_FULL_ARGS"'
} | run_as_root tee "$DROPIN" >/dev/null

echo "--- $DROPIN ---"
cat "$DROPIN"
echo

if command -v update-grub >/dev/null 2>&1; then
	run_as_root update-grub
elif command -v grub-mkconfig >/dev/null 2>&1; then
	run_as_root grub-mkconfig -o /boot/grub/grub.cfg
else
	echo "ERROR: neither update-grub nor grub-mkconfig is available" >&2
	exit 1
fi

echo
echo "configured=1"
echo "reboot=$REBOOT"

if [[ "$REBOOT" == "1" ]]; then
	echo "rebooting_now=1"
	nohup bash -c 'sleep 2; reboot' >/tmp/llmes-nohz-reboot.log 2>&1 &
fi
ENDSSH
SSH_RC=$?
set -e

cat "$LOCAL_RESULT_DIR/setup.log"

if [[ "$REBOOT" != "1" || "$WAIT_AFTER_REBOOT" != "1" ]]; then
	echo "Done."
	echo "  Result dir: $LOCAL_RESULT_DIR"
	exit "$SSH_RC"
fi

echo ""
echo "Waiting for SSH after reboot..."
deadline=$((SECONDS + SSH_WAIT_SECONDS))
while (( SECONDS < deadline )); do
	if ssh "${SSH_OPTS[@]}" "${SSH_USER}@${SERVER_IP}" true >/dev/null 2>&1; then
		break
	fi
	sleep 5
done

if ! ssh "${SSH_OPTS[@]}" "${SSH_USER}@${SERVER_IP}" true >/dev/null 2>&1; then
	echo "ERROR: SSH did not come back within ${SSH_WAIT_SECONDS}s" >&2
	exit 1
fi

ssh "${SSH_OPTS[@]}" "${SSH_USER}@${SERVER_IP}" \
	"ISOLATED_CPUS='$ISOLATED_CPUS' HOUSEKEEPING_CPUS='$HOUSEKEEPING_CPUS' bash -s" \
	> "$LOCAL_RESULT_DIR/verify.log" 2>&1 <<'ENDSSH'
set -euo pipefail

echo "===== nohz_full verification ====="
echo "cmdline=$(cat /proc/cmdline)"
echo
for f in /sys/devices/system/cpu/nohz_full /sys/devices/system/cpu/isolated /sys/devices/system/cpu/online; do
	[[ -r "$f" ]] && echo "$f=$(cat "$f")"
done
echo
echo "expected_isolated_cpus=$ISOLATED_CPUS"
echo "expected_housekeeping_cpus=$HOUSEKEEPING_CPUS"
echo
echo "--- relevant cmdline args ---"
tr ' ' '\n' < /proc/cmdline | grep -E '^(nohz_full|rcu_nocbs|isolcpus|irqaffinity|kthread_cpus|nowatchdog|nmi_watchdog)=' || true
ENDSSH

cat "$LOCAL_RESULT_DIR/verify.log"
echo ""
echo "Done."
echo "  Result dir: $LOCAL_RESULT_DIR"
